package tr.edu.medipol.yazilimortamaraclari.MehmetFinal;

public class Product {

    private String name;
    private String no;
    
    public Product(String name, String no) {
        super();
        this.name = name;
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public String getNo() {
        return no;
    }

}
